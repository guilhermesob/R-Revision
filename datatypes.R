# Numeric data type
numer_num <- 19.6
class(numer_num)
# Integer data type
int_num <- 200L
class(int_num)
# Complex data type
comp_num <- 1 + 3i
class(comp_num)
# Character data type(String)
new_str <- "The power of R"
class(new_str)
# Boolean data type
x <- TRUE
y <- FALSE
class(x)
class(y)
